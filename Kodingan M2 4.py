#1. Menghitung Debit 1.0

print("masukkan nilai volume (satuan cm^3) : ")
volume = float(input())
print("Masukkan waktu (satuan detik) : ")
waktu = float(input())
debit = volume / waktu
print("jadi hasil debitnya adalah " + str(debit))

